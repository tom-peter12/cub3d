#include "../includes/libft.h"

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	h1;
	int	h2;

	h1 = *a;
	h2 = *b;
	if (h2 != 0)
	{
		*a = h1 / h2;
		*b = h1 % h2;
	}
}
